const HomepageFYQ = () => {
  const questions = [
    {
      id: 1,
      title: "Q-1:- How To Apply for Internships?",
      content: `To apply for the upcoming batch of internships, you need to fill out the interest form in that specific domain. After that, you will receive the selection certificate from us if selected after the interview.`,
    },
    {
      id: 2,
      title: "Q-2:- How To Fill the Interest Form?",
      content: `You will find the interest form for all the internship domains in the internship column. Click on the 'Apply Now' button, and your response will help us understand your current skills and any need for training during the internship. `,
    },
    {
      id: 3,
      title: "Q-3:- I Have Completed All the Necessary Steps, Whatâ€™s Next?",
      content: `If you have filled all the required fields in the interest form correctly, you will receive a selection certificate from us before your internship starts. All important information and announcements related to the internship are communicated through the discussion from IIT Academy Network on LinkedIn.`,
    },
    {
      id: 4,
      title: "Q-4:- What Will Be The Duration Of Internship?",
      content: `In every domain, the duration of the internship is 4 weeks.`,
    },
    {
      id: 5,
      title:
        "Q-5:- I Have Completed My Tasks Well Before Deadline. When Will I Get A Certificate?",
      content: `You can submit your task through the task submission form if you have received it or wait for it to be shared. If you have submitted the task before the deadline, please expect your completion certificate in the first week of the next month.`,
    },
  ];
  const Question = ({ question }) => {
    const initiallyOpen = question.id === 1 || question.id === 2;
    const [open, setOpen] = useState(initiallyOpen);

    const contentWithLineBreaks = question.content
      ? question.content.split("\n").map((line, index) => (
          <React.Fragment key={index}>
            {line}
            <br />
          </React.Fragment>
        ))
      : null;

    return (
      <AccordionSection
        className={`accordion ${open ? "open" : "closed"}`}
        id={`accordion-${question.id}`}
        onClick={() => setOpen(!open)}
      >
        <AccordionHeader
          className={open ? "open" : "closed"}
          id="accordion-two"
        >
          <QuestionTitle>{question.title}</QuestionTitle>
          <AccordionButton
            onClick={(e) => {
              e.stopPropagation(); // Prevents the parent click event
              setOpen(!open);
            }}
            className="btns"
          >
            {open ? <CaretUp /> : <CaretDown />}
          </AccordionButton>
        </AccordionHeader>
        {open && <AccordionPara>{contentWithLineBreaks}</AccordionPara>}
      </AccordionSection>
    );
  };
  return (
    <>
      <ContainerAccordion>
        <h1>Frequently Asked Questions</h1>
        <div className="questions">
          {questions.map((question) => (
            <Question key={question.id} question={question} />
          ))}
        </div>
      </ContainerAccordion>
    </>
  );
};

export default HomepageFYQ;
